class SuccessFactorsServiceIs extends SuccessFactorsWsdlClass
{
	/**
	 * Method to call the operation originally named isValidSession
	 * @uses SuccessFactorsWsdlClass::getSoapClient()
	 * @uses SuccessFactorsWsdlClass::setResult()
	 * @uses SuccessFactorsWsdlClass::getResult()
	 * @uses SuccessFactorsWsdlClass::saveLastError()
	 * @return SuccessFactorsStructIsValidSessionResponse
	 */
	public function isValidSession()
	{
		try
		{
			$this->setResult(new SuccessFactorsStructIsValidSessionResponse(self::getSoapClient()->isValidSession()));
		}
		catch(SoapFault $soapFault)
		{
			return !$this->saveLastError(__METHOD__,$soapFault);
		}
		return $this->getResult();
	}
	/**
	 * Returns the result
	 * @see SuccessFactorsWsdlClass::getResult()
	 * @return SuccessFactorsStructIsValidSessionResponse
	 */
	public function getResult()
	{
		return parent::getResult();
	}
	/**
	 * Method returning the class name
	 * @return string __CLASS__
	 */
	public function __toString()
	{
		return __CLASS__;
	}
}
